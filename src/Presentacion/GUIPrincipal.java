    private Object TipoDeCancha(int Seleccion) {
        switch(Seleccion){
            case 0:
                return new CdeFutbol();
            case 1:
                return new CdeFutbolSala();
            case 2:
                return new CdeTenis();
            case 3:
                return new CdeBaloncesto();
            case 4:
                return new CdeVolleybol();
        }
        return null;
    }
    private String FechaActual(){
        Calendar Fecha = new GregorianCalendar();
        String FechaActual = Fecha.get(Calendar.DAY_OF_MONTH)+"/"+(Fecha.get(Calendar.MONTH)+1)+"/"+Fecha.get(Calendar.YEAR);
        return FechaActual;
    }

    private short HoraInicio() {
        switch(cbxHorario.getSelectedIndex()){
            case 0:
                return 8;
            case 1:
                return 9;
            case 2:
                return 10;
            case 3:
                return 11;
            case 4:
                return 12;
            case 5:
                return 13;
            case 6:
                return 14;
            case 7:
                return 15;
            case 8:
                return 16;
            case 9:
                return 17;
        }
        return 0;
    }
