    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
        try{
            int Codigo = Integer.parseInt(txtCodigo.getText());
            Object TypeCancha = TipoDeCancha(cbxCancha.getSelectedIndex());
            String Fecha = FechaActual();
            short Hora = HoraInicio();
            if(Controlador.registrarReserva(Codigo,TypeCancha,Fecha,Hora)){
                JOptionPane.showMessageDialog(null, "Registro de Cancha Satisfactorio", "Registro Satisfactorio", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Error al intentar hacer el registro", "Registro Incorrecto", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Debe especificar un Codigo", "Registro Incorrecto", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnReservarActionPerformed

